package Data;

/**
 *
 * @author sinanjasar
 */
public class User {
    public String email;
    public String password;

    public User(String email, String password) {
        this.email = email;
        this.password = password;
    }

    public String getEmail() {
        return email;
    }

    public String getPassword() {
        return password;
    }

    @Override
    public String toString() {
        return "User{" + "email=" + email + ", password=" + password + '}';
    }
    
    
    
}
